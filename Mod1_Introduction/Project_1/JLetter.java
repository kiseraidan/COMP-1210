/**
* Simple program to print a large letter 'J' spelled out by the word 'JAVA'
* over the span of ten lines.
*
* @author Aidan Kiser - COMP1210 - 001
* @version 23 August 2022
*/ 
public class JLetter
{
   /**
   * Prints a large letter 'J' composed of the word 'JAVA' to std output.
   * @param args Command line arguments (not used).
   */
   public static void main(String[] args)
   {
      // The next ten lines print the large 'J.'
      System.out.println("JAVAJAVAJAVA");
      System.out.println("JAVAJAVAJAVA");
      System.out.println("      JAVA");
      System.out.println("      JAVA");
      System.out.println("      JAVA");
      System.out.println("      JAVA");
      System.out.println("J     JAVA");
      System.out.println("JA    JAVA");
      System.out.println(" JAVAJAVA");
      System.out.println("  JAVAJA");
   }
}