/**
* Simple program to print author's short term, medium term, as well as long
* term goals.
*
* Module 1: Project 1 'MyLifeGoals'
* @author Aidan Kiser - COMP1210 - 001
* @version 23 August 2022
*/
public class MyLifeGoals
{
   /**
   * Prints author's goals to std output.
   * @param args Command line arguments (not used).
   */
   public static void main(String[] args)
   {
      // Prints author's name.
      System.out.println("Aidan Kiser");
      // Prints author's short term goal after a line of white space.
      System.out.println("\nShort Term: I want to be able to excel in this "
         + "class as well as my other classes over the course of the semester"
         + " demonstrated by satisfactory grades.");
      // Prints author's medium term goal.
      System.out.println("Medium Term: Upon my graduation I plan to leave the"
         + " United States to settle in Japan. I plan to work for a Japanese "
         + "software company.");
      // Prints author's long term goal.
      System.out.println("Long Term: I want to be a successful senior "
         + "software engineer at a large software company demonstrating my "
         + "knowledge and skills acquired over the span of my life.");
   }
}